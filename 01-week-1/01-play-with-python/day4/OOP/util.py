


class Util:
    def __init__(self):
        pass

    @classmethod    
    def add(cls, a, b):
        return a + b
    @classmethod
    def sub(cls, a, b):
        return a - b
    @classmethod
    def mul(cls, a, b):
        return a * b
    @classmethod
    def div(cls, a, b):
        return a / b
    


